        <h2>Ear Training Practice</h2>
        <div>
            <p>These exercises will improve your musical ability by developing a more intuitive understanding of what you
                hear. For best results, practice a little bit every day:</p>
            <ul>
                <li>
                    Intervals: In this exercise, you will hear two notes in sequence. Your goal is to identify the interval
                    between the two notes.
                </li>
                <li>
                    Chords: In this exercise, you will hear a chord. Your goal is to identify the type of chord that you
                    heard.
                </li>
                <li>
                    Scales: In this exercise, you will hear a scale. Your goal is to identify the name of the scale that you
                    heard.
                </li>
                <li>
                    Chord Progressions: In this exercise, you will hear a chord progression. Your goal is to identify each
                    chord that you heard.
                </li>
                <li>
                    Perfect Pitch: In this exercise, you will hear a single note. Your goal is to identify the name of the
                    note.
                </li>
                <li>
                    Scale Degrees (functional): In this exercise, you will hear a short chord progression followed by a
                    single note. You must identify the scale degree of that note relative to the key established by the
                    chord progression. This is also known as "functional ear training."
                </li>
                <li>
                    Intervals in Context (functional): This exercise combines the "Intervals" and "Scale Degrees" exercises.
                    In this exercise, you will hear a short chord progression followed by two notes. You must identify the
                    major scale degrees of the two notes relative to the key established by the chord progression as well as
                    the interval between the two notes.
                </li>
                <li>
                    Melodic Dictation: In this exercise, you will hear a short chord progression followed by a short melody.
                    You must identify the major scale degree of each note in the melody.
                </li>
            </ul>
